// criando um array (coleção)
let pokemon = ['pikachu', 'charmander', 'bulbassaur']
console.log(pokemon)

// pegando apenas um dos elementos do vetor
console.log(pokemon[1])

// removendo o último elemento de um array usando o método pop()
pokemon.pop();
console.log(pokemon)

// removendo o primeiro elemento de um array usando o método shift()
pokemon.shift();
console.log(pokemon)